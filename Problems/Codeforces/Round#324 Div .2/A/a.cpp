#include <bits/stdc++.h>
#include <iostream>
using namespace std;

typedef long long ll;

int main() {

	ll n, t;
	cin >> n >> t;
	vector<int> num(n,0);
	num[0] = t;
	if(t == 10)
		num[0] = 1;

	if(n == 1 && t == 10) {
		cout << "-1" << endl;
		return 0;
	}

	

	for(int i = 0; i < n; i++)
		cout << num[i];
	cout << endl;
	
	return 0;
	

}
